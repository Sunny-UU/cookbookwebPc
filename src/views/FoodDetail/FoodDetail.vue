                                烹饪技巧
                                另外，有人会偏好切开带血水的白切鸡，方法是先将水烧开，再把鸡放进去煮15分钟，马上离火，焖10分钟后捞起，待不烫手后抹上麻油或色拉油切块。如果想口感更有弹性，可以把切好的鸡放到冰箱里冷藏一会再食用。</p>

